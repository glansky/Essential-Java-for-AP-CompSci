/**
 * Sprint 37 - The while Loop
 */
public class Sprint37 {
    public static void main(String[] args){
        int val = 0;

        // This loop will run as long as the condition at the top is always true
        while (val < 10){
            System.out.println(String.format("val: %d",val));
            val++;
        }
    }
}
